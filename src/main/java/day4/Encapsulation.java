package day4;

public class Encapsulation {
    private int a=10;
    private String s="java";

    public String getS() {
        return s;
    }

    public void setA(int a) {
        this.a = a;
    }

    public void setS(String s) {
        this.s = s;
    }

    public int getA() {
        return a;
    }
}
